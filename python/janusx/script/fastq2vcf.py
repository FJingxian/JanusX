def _missing_binaries(commands: Sequence[str]) -> List[str]:
    return [cmd for cmd in commands if shutil.which(cmd) is None]


def _missing_jx_tool_wrappers(commands: Sequence[str]) -> List[str]:
    jx_bin = shutil.which("jx")
    if jx_bin is None:
        return [str(x) for x in list(commands or [])]

    def _wrapper_missing_by_output(text: str) -> bool:
        msg = str(text or "").lower()
        if msg == "":
            return False
        if "please run `jx -update dlc`" in msg:
            return True
        if "dlc runtime is not ready" in msg:
            return True
        if "dlc runtime does not contain" in msg:
            return True
        if "dlc env runtime is missing" in msg:
            return True
        if "unsupported dlc tool" in msg:
            return True
        if "unknown module:" in msg:
            return True
        return False

    missing: List[str] = []
    for tool in commands:
        t = str(tool).strip()
        if t == "":
            continue
        try:
            p = subprocess.run(
                [str(jx_bin), t, "-h"],
                stdout=subprocess.PIPE,
                stderr=subprocess.PIPE,
                text=True,
                check=False,
                timeout=8,
            )
        except Exception:
            missing.append(t)
            continue
        if int(p.returncode) == 0:
            continue
        merged = f"{p.stdout or ''}\n{p.stderr or ''}"
        if _wrapper_missing_by_output(merged):
            missing.append(t)
    return missing


