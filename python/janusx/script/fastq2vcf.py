def _collect_docker_mounts(reference: Path, fastq_dir: Path, workdir: Path) -> List[Path]:
    mounts: List[Path] = []
    seen: set[str] = set()
    for p in (reference.parent, fastq_dir, workdir):
        rp = Path(p).expanduser().resolve()
        key = str(rp)
        if key in seen:
            continue
        seen.add(key)
        mounts.append(rp)
    return mounts


def _docker_exec_prefix(image_tag: str, workdir: Path, mounts: Sequence[Path]) -> str:
    parts: List[str] = ["docker", "run", "--rm"]
    try:
        uid = int(os.getuid())
        gid = int(os.getgid())
        parts.extend(["--user", f"{uid}:{gid}"])
    except Exception:
        pass

    for mount in mounts:
        m = str(Path(mount).expanduser().resolve())
        parts.extend(["-v", f"{m}:{m}"])

    parts.extend(["-w", str(workdir), image_tag])
    return " ".join(shlex.quote(x) for x in parts)


def _count_dockerfile_run_steps(dockerfile: Path) -> int:
    try:
        lines = dockerfile.read_text(encoding="utf-8", errors="ignore").splitlines()
    except Exception:
        return 0

    count = 0
    stmt = ""
    for raw in lines:
        s = str(raw).strip()
        if s == "" or s.startswith("#"):
            continue

        if stmt == "":
            stmt = s
        else:
            stmt = f"{stmt} {s}"

        if s.endswith("\\"):
            stmt = stmt[:-1].rstrip()
            continue

        head = stmt.split(None, 1)[0].upper() if stmt else ""
        if head == "RUN":
            count += 1
        stmt = ""

    if stmt:
        head = stmt.split(None, 1)[0].upper()
        if head == "RUN":
            count += 1
    return max(0, int(count))


